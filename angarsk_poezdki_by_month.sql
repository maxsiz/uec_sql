SELECT R.NAME, N_MONTH, SUM(VSEGO) as VSEGO, SUM(platno) as plat, SUM(espb) as ESP FROM (
SELECT D.NAME,TO_CHAR(TRUNC(T.DATE_OF,'MM'), 'DDMMYYYY') as N_MONTH
 ,1 as VSEGO
 ,CASE
    when T.CARD_SERIES  in (11,12,15,20,21,22,23,25,26,27,28,30,31,32,33,51,52,53,54,55,56,62,64,65) then 1
    else 0
 END as platno
 ,CASE
    when T.CARD_SERIES  in (60,63) then 1
    else 0
 END as espb
 --T.CARD_NUM, T.date_of, C.NUM, C.*, 
 --T.*
 FROM CPTT.T_DATA T
 --LEFT JOIN CPTT.CARD C ON T.ID_CARD=C.ID
   INNER JOIN CPTT.DIVISION D ON T.ID_DIVISION=D.ID
   INNER JOIN CPTT.OPERATOR O ON D.ID_OPERATOR=O.ID  and O.ROLE=1
 WHERE 
     --T.CARD_SERIES  in (10,11,12,13,15,20,21,23,25,26,27,28,30,31,32,33,51,52,53,54,55,56,62,64,65)  
     --T.CARD_SERIES  in (60,63) 
     O.CODE = '0019'  
AND  (T.KIND=16 OR T.KIND=17 OR T.KIND=18 OR T.KIND=19)
--AND  (T.KIND=10 OR T.KIND=7 OR T.KIND=8 OR T.KIND=11 OR T.KIND=12 OR T.KIND=13  OR T.KIND=14  OR T.KIND=15)
--and T.DATE_OF >=TO_DATE('20150718', 'YYYYMMDD') and  T.DATE_OF < TO_DATE('20150812', 'YYYYMMDD')
-- AND TO_CHAR(TRUNC(T.DATE_OF,'DD'), 'DDMMYYYY') = '28082015'
--AND T.card_num > '0300671110'
 and T.DATE_OF BETWEEN TO_DATE('01.01.2015 00:00:01','DD.MM.YYYY HH24:MI:SS')
                       AND TO_DATE('30.09.2015 23:59:59','DD.MM.YYYY HH24:MI:SS')
) R                       
GROUP BY  R.NAME, R.N_MONTH
ORDER BY  R.NAME, R.N_MONTH


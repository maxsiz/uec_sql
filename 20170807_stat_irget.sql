--stat poezdok
SELECT D.NAME, count (*) as kol_t
 FROM CPTT.T_DATA T
    INNER JOIN CPTT.DIVISION D ON T.ID_DIVISION=D.ID
    INNER JOIN CPTT.OPERATOR O ON D.ID_OPERATOR=O.ID  and O.ROLE=1
 WHERE 
    nvl(T.NEW_CARD_SERIES, T.CARD_SERIES)  in (84) --+67,84  from may, -67 from 201707
     --and O.CODE in ('0002','0003','0004','0005','0006','0007','0008','0009','0011','0012','0013','0014'
    --                ,'0015','0016','0017','0018','0019','0020','0021','0022','0023','0024','0025','0026','0027','0028','0062'
--                    ,'0080'--Usolie
         --           ) 
and   O.CODE ='0030'
AND   T.KIND=17 
AND TO_CHAR(TRUNC(T.DATE_OF,'MM'), 'DDMMYYYY') = '01072017'
AND T.INS_DATE < TO_DATE('02.08.2017  9:27:55','DD.MM.YYYY HH24:MI:SS') 
GROUP BY D.NAME

--stat card_sec
SELECT  D.NAME, T.CARD_NUM_SEC, count (*) as kol_t
FROM CPTT.T_DATA T
    INNER JOIN CPTT.DIVISION D ON T.ID_DIVISION=D.ID
    INNER JOIN CPTT.OPERATOR O ON D.ID_OPERATOR=O.ID  and O.ROLE=1
 WHERE 
    nvl(T.NEW_CARD_SERIES, T.CARD_SERIES)  in (84) --+67,84  from may, -67 from 201707
and   O.CODE ='0030'
AND   T.KIND=17 
AND TO_CHAR(TRUNC(T.DATE_OF,'MM'), 'DDMMYYYY') = '01072017'
AND T.INS_DATE < TO_DATE('02.08.2017  9:27:55','DD.MM.YYYY HH24:MI:SS') --month close date
GROUP BY  D.NAME, T.CARD_NUM_SEC
order by T.CARD_NUM_SEC
